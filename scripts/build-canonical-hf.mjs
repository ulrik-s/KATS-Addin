#!/usr/bin/env node
// @ts-check
/**
 * Bake the firm's canonical header/footer images into a TypeScript
 * module so they can be embedded directly into HTML inserted via
 * Office.js Body.insertHtml() — no runtime fetch needed, no CORS, no
 * dev-vs-prod URL juggling.
 *
 * Source images live in `templates/headers-footers/` (committed to
 * the repo as the firm's brand assets). Output is a generated TS
 * module the runtime imports.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = fileURLToPath(new URL('.', import.meta.url));
const root = resolve(here, '..');

const sources = {
  logo: { file: 'templates/headers-footers/logo.png', mime: 'image/png' },
  firmInfo: { file: 'templates/headers-footers/firm-info-image.jpeg', mime: 'image/jpeg' },
};

const outPath = resolve(root, 'src/app/headers-footers/canonical-images.generated.ts');

const lines = [
  '// AUTO-GENERATED by scripts/build-canonical-hf.mjs — do not edit by hand.',
  '// Run `yarn build:canonical-hf` to regenerate after changing the source',
  '// images in templates/headers-footers/.',
  '',
];

for (const [exportName, { file, mime }] of Object.entries(sources)) {
  const buf = readFileSync(resolve(root, file));
  const base64 = buf.toString('base64');
  lines.push(
    `/** ${file} (${String(buf.length)} bytes) as a base64 data URI. */`,
    `export const ${exportName.toUpperCase()}_DATA_URL =`,
    `  'data:${mime};base64,${base64}';`,
    '',
  );
}

mkdirSync(resolve(root, 'src/app/headers-footers'), { recursive: true });
writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log(`canonical-images.generated.ts written → ${outPath}`);
